"""
Bitwise operation are OR, AND, NOT, EX-OR, RIGHT SHIFT and LEFT SHIFT
"""

a = 3
b = 4

# -- And operation ---#
print(a & b)

# --- OR operation ---#
print(a | b)

# --- EX-OR opeartion ---#
print(a ^ b)

# --- Left shift ---#
print(a << 1)
print(a << 2)

# -- Right shift ---#\
print(b >> 1)
print(b >> 2)